function filter(){
    let table = document.getElementById("cars");

    //if(table.rows.length != 0){
        let make = document.getElementById("make");
        let selectedMake = make.options[make.selectedIndex].text;

        let model = document.getElementById("model");
        let selectedModel = model.options[model.selectedIndex].text;

        let fuel = document.getElementById("fuel");
        let selectedFuel = fuel.options[fuel.selectedIndex].text;

        let transmission = document.getElementById("transmission");
        let selectedTransmission = transmission.options[transmission.selectedIndex].text;

        let location = document.getElementById("location");
        let selectedLocation = location.options[location.selectedIndex].text;

        document.getElementById("cars").innerHTML = "";

        if(selectedMake == "Any" && selectedModel == "Any" && selectedFuel == "Any" && selectedTransmission == "Any" && selectedLocation == "Any"){
          renderCars(cars);
          return;
        }

        let filteredCars = cars;

        if(selectedMake != "Any")
          filteredCars = filteredCars.filter(car => car.Make == selectedMake);
        if(selectedModel != "Any")
          filteredCars = filteredCars.filter(car => car.Model == selectedModel);
        if(selectedFuel != "Any")
          filteredCars = filteredCars.filter(car => car.Fuel == selectedFuel);
        if(selectedTransmission != "Any")
          filteredCars = filteredCars.filter(car => car.Transmission == selectedTransmission);
        if(selectedLocation != "Any")
          filteredCars = filteredCars.filter(car => car.Location == selectedLocation);

        renderCars(filteredCars);
    //}
}

function addRow(car, filters, index){
    let table = document.getElementById("cars");

    if(table.rows.length == 0){
        let row = table.insertRow(-1);

        for (let key in car) {
            row.insertCell(-1).outerHTML = `<th>${key}</th>`;
        }
    }

    let row = table.insertRow(-1);
    row.id = index;
    
    for (let key in car) {
       row.insertCell(-1).innerHTML = car[key];
      
       switch(key){
            case "Make":
                filters.make.add(car[key]);
                break;
            case "Model":
                filters.model.add(car[key]);
                break;
            case "Fuel":
                filters.fuel.add(car[key]);
                break;
            case "Transmission":
                filters.transmission.add(car[key]);
                break;
            case "Location":
              filters.location.add(car[key]);
              break;
            default:
                break;
       }
    }

    row.insertCell(-1).innerHTML = `<button class="selectBtn" onclick="selectCar(${row.id})">Select</button>`;
    
}

